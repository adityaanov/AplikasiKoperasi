    private void btn_lihatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_lihatActionPerformed
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        SimpleDateFormat sDate = new SimpleDateFormat("yyyy-MM-dd");
        String date = sDate.format(jc_tanggal.getDate());
        try{
            Connection c = Koneksi.KoneksiDb();
            String sql = "Select * From kasir Where tanggal = '"+date+"'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8),
                    rs.getString(9),
                });
            }tabellaporanpendapatan.setModel(model);
            TotalBiaya();
        }catch(Exception e){
            System.out.println("Cari Data Error"+e.getMessage());
        }finally{
            jc_tanggal.setDate(null);
        }
    }//GEN-LAST:event_btn_lihatActionPerformed

    private void btn_caritanggalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_caritanggalActionPerformed
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        SimpleDateFormat sDate = new SimpleDateFormat("yyyy-MM-dd");
        String date1 = sDate.format(jc_drtgl.getDate());
        String date2 = sDate.format(jc_drtgl2.getDate());
        try{
            Connection c = Koneksi.KoneksiDb();
            String sql = "Select * From kasir Where tanggal Between '"+date1+"' And '"+date2+"'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8),
                    rs.getString(9),
                });
            }tabellaporanpendapatan.setModel(model);
            TotalBiaya();
        }catch(Exception e){
            System.out.println("Cari Data Error"+e.getMessage());
        }finally{
            jc_drtgl.setDate(null);
            jc_drtgl2.setDate(null);
        }
    }//GEN-LAST:event_btn_caritanggalActionPerformed

