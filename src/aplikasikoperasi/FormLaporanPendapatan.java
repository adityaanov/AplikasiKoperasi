    public void TotalBiaya(){
        int jumlahBaris = tabellaporanpendapatan.getRowCount();
        int totalBiaya = 0;
        int total;
        for(int i = 0; i < jumlahBaris; i++){
            total = Integer.parseInt(tabellaporanpendapatan.getValueAt(i, 6).toString());
            totalBiaya = totalBiaya + total;
        }
        txt_totalpendapatan.setText(String.valueOf("Rp "+totalBiaya+",-"));
    }
    
